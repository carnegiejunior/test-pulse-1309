#CLIENTES

CREATE TABLE IF NOT EXISTS db_mateus.clientes(
id BIGINT NOT NULL PRIMARY KEY AUTO_INCREMENT,
nome varchar(45) NOT NULL
) ENGINE=InnoDB DEFAULT CHARACTER SET = utf8;


#USUARIOS

CREATE TABLE IF NOT EXISTS db_mateus.usuarios(
id BIGINT NOT NULL PRIMARY KEY AUTO_INCREMENT,
nome varchar(45) NOT NULL
) ENGINE=InnoDB DEFAULT CHARACTER SET = utf8;